#include "frame.h"
#include "manager.h"
#include "renderer.h"
#include "texture.h"

//=============================================================================
// コンストラクタ
//=============================================================================
CFrame::CFrame()
{
	m_fFecrease = 0.0f;
}

//=============================================================================
// デストラクタ
//=============================================================================
CFrame::~CFrame()
{

}

//=============================================================================
// 生成
//=============================================================================
CFrame* CFrame::Create()
{
	CFrame* pFrame = nullptr;

	if (pFrame == nullptr)
	{
		pFrame = new CFrame;

		pFrame->Init();
	}

	return nullptr;
}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CFrame::Init(void)
{
	CObject2D::Init();
	SetSize(SCREEN_WIDTH, SCREEN_HEIGHT);
	BindTexture(CTexture::GetInstance()->Regist("data\\TEXTURE\\moya.png"));

	return E_NOTIMPL;
}

//=============================================================================
// 終了処理
//=============================================================================
void CFrame::Uninit(void)
{
	CObject2D::Uninit();
}

//=============================================================================
// 更新処理
//=============================================================================
void CFrame::Update(void)
{
	CObject2D::Update();
}

//=============================================================================
// 描画処理
//=============================================================================
void CFrame::Draw(void)
{
	CObject2D::Draw();;
}
