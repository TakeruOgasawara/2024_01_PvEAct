#include "UI.h"


//===========================================================================================
// コンストラクタ
//===========================================================================================
CUI::CUI(int nPriority) : CObject2D(nPriority)
{

}

//===========================================================================================
// デストラクタ
//===========================================================================================
CUI::~CUI()
{
	
}

//===========================================================================================
// 生成
//===========================================================================================
CUI* CUI::Create()
{
	CUI* pUI = nullptr;

	if (pUI == nullptr)
	{
		pUI = new CUI;

		pUI->Init();
	}

	return pUI;
}

//===========================================================================================
// 初期化処理
//===========================================================================================
HRESULT CUI::Init()
{
	CObject2D::Init();

	return E_NOTIMPL;
}

//===========================================================================================
// 終了処理
//===========================================================================================
void CUI::Uninit()
{
	CObject2D::Uninit();
}

//===========================================================================================
// 更新処理
//===========================================================================================
void CUI::Update()
{
	CObject2D::Update();
}

//===========================================================================================
// 描画処理
//===========================================================================================
void CUI::Draw()
{
	CObject2D::Draw();
}
